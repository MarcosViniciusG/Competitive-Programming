#include <bits/stdc++.h>

using namespace std;
int main() {
    int a, b;
    cin >> a >> b;

    cout << "a = " << a << endl;
    cout << "b = " << b << endl;

    cout << "AND = " << (a & b) << endl;
    cout << "OR = " << (a | b) << endl;
    cout << "XOR = " << (a ^ b) << endl;
    cout << "NOT = " << (~a) << endl;

}